package pageObjects;

public class Plate
{

    public int odds;
    public int even;
    public String element;
    public String colorName;
    public String col;
    public String salad;


    public Plate(int odds, int even, String element, String colorName, String col, String salad) {
        this.odds = odds;
        this.even = even;
        this.element = element;
        this.colorName = colorName;
        this.col = col;
        this.salad = salad;
    }
}
